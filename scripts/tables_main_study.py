"""Paper-ready LaTeX tables for the main study, driven entirely from the on-disk
analysis JSONs (no hardcoded metrics). Each emitted file is a complete float that
the paper imports via \\input{generated/...}; existing \\label keys are preserved
so cross-references keep resolving after the inline tables are swapped out.

Outputs into code/outputs/main_study/export/ (the staging dir promoted to
thesis-msc/generated/ by make export-assets):
  mainstudy_quality_ci.tex   -> tab:results-arch-mean   (per-arch quality + 95% CI)
  mainstudy_cost.tex         -> tab:results-cost-quality (deployment cost, both cards)
  mainstudy_cost_decomposition.tex -> tab:results-cost-decomp (build/answer x LLM/embed)
  mainstudy_breakeven.tex    -> tab:results-breakeven    (break-even N* per doc)
  mainstudy_significance.tex -> tab:results-significance (paired clustered bootstrap)
The three figure PDFs/PNGs are also copied in with stable paper names.
"""
from __future__ import annotations

import json
import shutil
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
MS = ROOT / "outputs" / "main_study"
EXPORT = MS / "export"
EXPORT.mkdir(parents=True, exist_ok=True)

ARCHS = ["flat", "naive_rag", "raptor", "graphrag"]
LABEL = {"flat": "Flat", "naive_rag": "Naive RAG", "raptor": "RAPTOR", "graphrag": "GraphRAG"}

sig = json.loads((MS / "significance.json").read_text(encoding="utf-8"))
cost = json.loads((MS / "cost_per_arch.json").read_text(encoding="utf-8"))
brk = json.loads((MS / "breakeven.json").read_text(encoding="utf-8"))
mem = json.loads((MS / "memorization_control.json").read_text(encoding="utf-8"))

QA = sig["datasets"]["qasper"]["per_arch"]
NV = sig["datasets"]["novelqa"]["per_arch"]
PC = cost["per_arch"]


def ci(d: dict) -> str:
    return f"{d['mean']:.3f} [{d['ci_low']:.3f}, {d['ci_high']:.3f}]"


HEADER = "% Auto-generated by code/scripts/tables_main_study.py from\n" \
         "% code/outputs/main_study/{significance,cost_per_arch,breakeven,memorization_control}.json\n" \
         "% Do not edit by hand; regenerate and re-export.\n"


def write(name: str, body: str) -> None:
    (EXPORT / name).write_text(HEADER + body, encoding="utf-8")
    print(f"  wrote {name}")


def texint(n: int) -> str:
    """Integer with a LaTeX-math thousands separator, e.g. 1348 -> 1{,}348."""
    return f"{n:,}".replace(",", "{,}")


def quality_table() -> None:
    rows = "\n".join(
        f"{LABEL[a]} & {ci(QA[a])} & {ci(NV[a])} \\\\" for a in ARCHS)
    qn, qc = texint(QA["flat"]["n_questions"]), QA["flat"]["n_clusters"]
    nn, nc = texint(NV["flat"]["n_questions"]), NV["flat"]["n_clusters"]
    body = rf"""\begin{{table}}[ht]
\centering
\caption{{Per-architecture answer quality on the held-out evaluation pools
($N=5$ repeats at $T=0$; QASPER ${qn}$ free-form questions over ${qc}$ papers,
NovelQA ${nn}$ multiple-choice questions over ${nc}$ novels,
calibration and held-out novels excluded).
Cells report the mean with a $95\%$ confidence interval from a paired
clustered bootstrap ($10{{,}}000$ resamples; clusters are papers for QASPER and
novels for NovelQA). QASPER is scored with the official Answer-F1 metric;
NovelQA with held-out Codabench accuracy. The ranking
Flat $>$ Naive RAG $\gtrsim$ RAPTOR $>$ GraphRAG is identical on both
datasets; there is no cross-dataset inversion.}}\label{{tab:results-arch-mean}}
\begin{{tabular}}{{lcc}}
\toprule
Architecture & QASPER Answer-F1 [95\% CI] & NovelQA accuracy [95\% CI] \\
\midrule
{rows}
\bottomrule
\end{{tabular}}
\end{{table}}
"""
    write("mainstudy_quality_ci.tex", body)


def cost_table() -> None:
    # order by base-card deployment total (ascending): naive, raptor, flat, graphrag
    order = sorted(ARCHS, key=lambda a: PC[f"base|{a}"]["total"])
    rows = []
    for a in order:
        b = PC[f"base|{a}"]
        c = PC[f"cache|{a}"]
        rows.append(
            f"{LABEL[a]} & {b['c_off_total']:.2f} & {b['c_on_total']:.2f} & "
            f"{b['total']:.2f} & {c['total']:.2f} & {b['c_on_per_query']*1000:.2f} \\\\")
    rows_tex = "\n".join(rows)
    base_tot = cost["deployment_total"]["base"]
    cache_tot = cost["deployment_total"]["cache"]
    body = rf"""\begin{{table}}[ht]
\centering
\caption{{Per-architecture deployment cost in USD on the primary answerer
(\texttt{{gemini-3.1-flash-lite-preview}}), accounted over the same post-exclusion
evaluation pool as the quality tables (calibration and held-out novels removed).
$C_{{\text{{off}}}}$ is the one-time index/build cost; $C_{{\text{{on}}}}$ is the
total per-query answering cost; the rightmost column is the marginal per-query
cost in milli-dollars (m\$), where one milli-dollar is $10^{{-3}}$~USD. Gemini calls are priced from the provider rate card and
embedding compute at the local GPU rate (Appendix~\ref{{app:protocol}}).
Both deployment columns are cache-aware---repeated reads are billed at a cached
rate in both---and differ only in that rate: Gemini's published one-quarter of the
uncached price (std-cache) versus a more aggressive one-tenth (deep-cache). The two
coincide for every architecture except Flat, because only Flat re-reads the full
document and so accrues cached input at all; even for Flat the deeper discount
touches only cached input, not the uncached first read or the output, so its total
falls by roughly two-fifths rather than ten-fold. The
cost ranking (Naive RAG $<$ RAPTOR $<$ Flat $<$ GraphRAG) is orthogonal to the
quality ranking, which is what the Pareto analysis
(Figure~\ref{{fig:pareto-cost-quality}}) resolves. Grid totals:
\${base_tot:.2f} (std-cache) / \${cache_tot:.2f} (deep-cache).}}\label{{tab:results-cost-quality}}
\begin{{tabular}}{{lrrrrr}}
\toprule
 & $C_{{\text{{off}}}}$ & $C_{{\text{{on}}}}$ & Deploy & Deploy & $C_{{\text{{on}}}}$/query \\
Architecture & (USD) & (USD) & std-cache (USD) & deep-cache (USD) & (m\$) \\
\midrule
{rows_tex}
\bottomrule
\end{{tabular}}
\end{{table}}
"""
    write("mainstudy_cost.tex", body)


def cost_decomposition_table() -> None:
    # Where each architecture's deployment dollars actually go: the one-time build
    # vs the per-query answering, each split into LLM API calls and local bge-m3
    # embedding. Standard card, same post-exclusion pool as cost_table. Driven
    # from cost_per_arch.json -- no hardcoded metrics.
    order = sorted(ARCHS, key=lambda a: PC[f"base|{a}"]["total"])
    rows = []
    for a in order:
        b = PC[f"base|{a}"]
        rows.append(
            f"{LABEL[a]} & {b['gemini_off']:.2f} & {b['embed_off']:.2f} & "
            f"{b['gemini_on']:.2f} & {b['embed_on']:.2f} & {b['total']:.2f} & "
            f"{b['c_store_total']:.3f} \\\\")
    rows_tex = "\n".join(rows)
    gr, rp = PC["base|graphrag"], PC["base|raptor"]
    f_base, f_cache = PC["base|flat"]["total"], PC["cache|flat"]["total"]
    # Deep-cache prices cached input at 0.1x uncached vs the standard card's 0.25x
    # (a 0.4x factor), so the standard->deep saving is 0.6x of the cached re-read
    # cost; invert it to recover the cached re-reads under the standard card and
    # the undiscounted remainder (uncached first read + output).
    cached_base = (f_base - f_cache) / 0.6
    other = f_base - cached_base
    body = rf"""\begin{{table}}[ht]
\centering
\caption{{Where each architecture's deployment dollars go, on the standard card and
the same post-exclusion pool as Table~\ref{{tab:results-cost-quality}}: the one-time
build ($C_{{\text{{off}}}}$) and the per-query answering ($C_{{\text{{on}}}}$), each split
into LLM API calls and local \texttt{{bge-m3}} embedding. Persistent-artifact storage
($C_{{\text{{store}}}}$) is shown separately---it folds into the study-wide amortized
cost, not the deployment total. All values in USD. Build cost is dominated by LLM
calls: GraphRAG's entity and community extraction (\${gr['gemini_off']:.2f}) and
RAPTOR's tree summarization (\${rp['gemini_off']:.2f}); embedding is a minor line
except RAPTOR's per-node tree vectors (\${rp['embed_off']:.2f}). Flat carries no
build and no embedding---its \${f_base:.2f} is entirely answering API, of which about
\${cached_base:.2f} is the cached re-reads the deep-cache card reprices and
\${other:.2f} is the uncached first read of each document plus all output, which no
cache discount touches.}}\label{{tab:results-cost-decomp}}
\begin{{tabular}}{{lrrrrrr}}
\toprule
 & \multicolumn{{2}}{{c}}{{Build ($C_{{\text{{off}}}}$)}} & \multicolumn{{2}}{{c}}{{Answer ($C_{{\text{{on}}}}$)}} & & \\
\cmidrule(lr){{2-3}}\cmidrule(lr){{4-5}}
Architecture & LLM & embed & LLM & embed & Deploy & Storage \\
 & (USD) & (USD) & (USD) & (USD) & (USD) & (USD) \\
\midrule
{rows_tex}
\bottomrule
\end{{tabular}}
\end{{table}}
"""
    write("mainstudy_cost_decomposition.tex", body)


def breakeven_table() -> None:
    flat_q = PC["base|flat"]["c_on_per_query"]
    rows = []
    for a in ("naive_rag", "raptor", "graphrag"):
        bb = brk[f"base|{a}"]
        bc = brk[f"cache|{a}"]
        nb = bb["n_star"]
        nc = bc["n_star"]
        nb_s = "any $N\\ge1$" if nb is not None and nb < 1 else (f"{nb:.1f}" if nb else "---")
        nc_s = "any $N\\ge1$" if nc is not None and nc < 1 else (f"{nc:.1f}" if nc else "---")
        rows.append(
            f"{LABEL[a]} & {bb['c_off_per_doc']*1000:.2f} & {bb['c_on_per_query']*1000:.3f} "
            f"& {nb_s} & {nc_s} \\\\")
    rows_tex = "\n".join(rows)
    body = rf"""\begin{{table}}[ht]
\centering
\caption{{Break-even query density versus cache-aware Flat. A structured
architecture amortizes its per-document build cost $C_{{\text{{off}}}}/\text{{doc}}$
over $N$ queries; $N^\star$ is the questions-per-document density at which its
amortized cost per query falls below Flat's marginal
\${flat_q*1000:.2f} per query. Reported under both price cards. Naive RAG is
cheaper than Flat at any density. RAPTOR and GraphRAG repay their build cost only
above $N^\star$: on the short QASPER workload ($\approx4$ q/paper) neither reaches
break-even, whereas on the long NovelQA workload ($\approx25$ q/novel) both do
(RAPTOR well past it, GraphRAG just above). Even where the build cost is recovered,
they answer no better than Flat and Naive RAG remains cheaper, so break-even never
converts into a quality or cost-position advantage.}}\label{{tab:results-breakeven}}
\begin{{tabular}}{{lrrcc}}
\toprule
 & $C_{{\text{{off}}}}$/doc & $C_{{\text{{on}}}}$/query & $N^\star$ & $N^\star$ \\
Architecture & (m\$) & (m\$) & std & cache \\
\midrule
{rows_tex}
\bottomrule
\end{{tabular}}
\end{{table}}
"""
    write("mainstudy_breakeven.tex", body)


def significance_table() -> None:
    def block(ds_key: str, ds_name: str) -> str:
        pw = sig["datasets"][ds_key]["pairwise"]
        out = [f"\\multicolumn{{5}}{{l}}{{\\textit{{{ds_name}}}}} \\\\"]
        for p in pw:
            star = "yes" if p["significant"] else "no"
            pv = p["p_value"]
            p_str = "$<0.001$" if pv < 0.001 else f"{pv:.3f}"
            out.append(
                f"{LABEL[p['a']]} vs {LABEL[p['b']]} & {p['mean_diff']:+.3f} & "
                f"[{p['ci_low']:+.3f}, {p['ci_high']:+.3f}] & {p_str} & {star} \\\\")
        return "\n".join(out)
    nr = next(p for p in sig["datasets"]["novelqa"]["pairwise"]
              if {p["a"], p["b"]} == {"naive_rag", "raptor"})
    body = rf"""\begin{{table}}[ht]
\centering
\caption{{Paired clustered-bootstrap significance ($10{{,}}000$ resamples,
percentile $95\%$ CI on the mean difference and the two-sided bootstrap $p$-value ($p<0.05$ counts as significant); clusters are papers for QASPER and
novels for NovelQA). Every QASPER pair is significant. On NovelQA every pair is
significant except Naive RAG versus RAPTOR (${nr['mean_diff']:+.3f}$, CI straddles zero),
which are statistically tied.}}\label{{tab:results-significance}}
\begin{{tabular}}{{lrccc}}
\toprule
Pair & $\Delta$ mean & 95\% CI of $\Delta$ & $p$ & Significant \\
\midrule
{block("qasper", "QASPER (Answer-F1)")}
\midrule
{block("novelqa", "NovelQA (accuracy)")}
\bottomrule
\end{{tabular}}
\end{{table}}
"""
    write("mainstudy_significance.tex", body)


def memorization_table() -> None:
    """Per-architecture quality vs the closed-book floor, both datasets."""
    nv, qa = mem["novelqa"], mem["qasper"]
    nv_floor, qa_floor = nv["closed_book"], qa["closed_book"]
    rows = [f"Closed-book floor & {qa_floor:.2f} & --- & {nv_floor:.2f} & --- \\\\", r"\midrule"]
    for a in ARCHS:
        n, q = nv["per_arch"][a], qa["per_arch"][a]
        rows.append(f"{LABEL[a]} & {q['answer_f1']:.2f} & {q['lift']:+.2f} & "
                    f"{n['accuracy']:.2f} & {n['lift']:+.2f} \\\\")
    rows_tex = "\n".join(rows)
    body = rf"""\begin{{table}}[ht]
\centering
\caption{{Memorization control: per-architecture answer quality against the
closed-book floor (the document \emph{{withheld}}; question, and options for NovelQA).
NovelQA's public-domain classics are recalled well above the $0.25$ four-way-MC
chance rate---closed-book accuracy is already {nv_floor:.2f}---so its absolute
scores are memorization-inflated, whereas QASPER's research papers are not recalled
(closed-book Answer-F1 {qa_floor:.2f}) and require the document. The reading lift
(with-document minus closed-book) measures the usable evidence each architecture
supplies: Flat lifts NovelQA accuracy by ${nv['per_arch']['flat']['lift']:+.2f}$,
whereas GraphRAG adds almost nothing (${nv['per_arch']['graphrag']['lift']:+.2f}$)
over closed-book guessing. The architecture ranking is the same with and without
the memorization caveat, so the comparison is not confounded by memorization
even though the NovelQA absolute level is.}}\label{{tab:results-memorization}}
\begin{{tabular}}{{lcccc}}
\toprule
 & \multicolumn{{2}}{{c}}{{QASPER Answer-F1}} & \multicolumn{{2}}{{c}}{{NovelQA accuracy}} \\
Architecture & with doc. & lift & with doc. & lift \\
\midrule
{rows_tex}
\bottomrule
\end{{tabular}}
\end{{table}}
"""
    write("mainstudy_memorization.tex", body)


def copy_figures() -> None:
    src = MS / "figures"
    for stem in ("pareto_cost_quality", "accuracy_by_arch", "breakeven_curves", "memorization_floor"):
        for ext in ("pdf", "png"):
            s = src / f"{stem}.{ext}"
            if s.exists():
                shutil.copy2(s, EXPORT / f"mainstudy_{stem}.{ext}")
        print(f"  copied figure mainstudy_{stem}.pdf/.png")


def main() -> int:
    print(f"tables + figures -> {EXPORT}")
    quality_table()
    cost_table()
    cost_decomposition_table()
    breakeven_table()
    significance_table()
    memorization_table()
    copy_figures()
    print(f"\nmemorization floor (for prose, not a table): nocontext "
          f"{mem['nocontext_accuracy']:.4f}, flat {mem['flat_accuracy']:.4f}, "
          f"lift {mem['lift']:+.4f}")
    print("done.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
